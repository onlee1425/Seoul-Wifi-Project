package seoul_WIFI;

public class History_DTO {

	private int id;
	private String lat;
	private String lnt;
	private String serch_date;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLnt() {
		return lnt;
	}
	public void setLnt(String lnt) {
		this.lnt = lnt;
	}
	public String getSerch_date() {
		return serch_date;
	}
	public void setSerch_date(String serch_date) {
		this.serch_date = serch_date;
	}
	
	
}
